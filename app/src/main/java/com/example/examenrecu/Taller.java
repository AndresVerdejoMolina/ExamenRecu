package com.example.examenrecu;

public class Taller {

    String marca1;
    String nombreTaller;


    public Taller(String marca1, String nombreTaller) {
        this.marca1 = marca1;
        this.nombreTaller = nombreTaller;
    }

    public String getMarca1() {
        return marca1;
    }

    public void setMarca1(String marca) {
        this.marca1 = marca;
    }

    public String getNombreTaller() {
        return nombreTaller;
    }

    public void setNombreTaller(String nombreTaller) {
        this.nombreTaller = nombreTaller;
    }



}
